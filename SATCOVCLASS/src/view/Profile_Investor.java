    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JPanel p = new JPanel();
        JTextField username = new JTextField(10);
        JTextField pass = new JTextField(10);
        JTextField confirmPass = new JTextField(10);
        
        p.add(new JLabel("Username :"));
        p.add(username);
        p.add(new JLabel("Password : "));
        p.add(pass);
        p.add(new JLabel("Confirm Password : "));
        p.add(confirmPass);
        JOptionPane.showConfirmDialog(null, p, "Add Stock : ", JOptionPane.OK_CANCEL_OPTION);
        if(pass.getText().equals(confirmPass.getText())){
            try {
                User.deleteAccount(username.getText(), pass.getText());
                JOptionPane.showMessageDialog(this, "Delete Account Success.");
                new Dashboard_Guest().setVisible(true);
                this.setVisible(false);
            } catch (Exception ex) {
                Logger.getLogger(ManageStock_Admin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Delete Account Failed.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

