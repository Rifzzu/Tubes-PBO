    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtNama.getText().equals("") || txtKode.getText().equals("") || txtHarga.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Field can't be empty!");
        }else{
            try {
                Admin.addStockExchange(txtKode.getText(), txtNama.getText(), Integer.parseInt(txtHarga.getText()));
                JOptionPane.showMessageDialog(this, "Add Stock Success.");
                new ManageStock_Admin(usr).setVisible(true);
                this.setVisible(false);
            } catch (Exception ex) {
                Logger.getLogger(ManageStock_Admin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

